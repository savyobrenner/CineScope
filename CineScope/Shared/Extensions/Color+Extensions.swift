import SwiftUI

extension Color {
    enum Brand {
        static let primary = Color("csPrimary")
        static let secondary = Color("csSecondary")
        static let background = Color("csBackground")
        static let white = Color("csWhite")
        static let placeholder = Color("csPlaceholder")
    }
}
